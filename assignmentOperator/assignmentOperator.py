# Assignment Operators - Complete Program


print("Assignment Operators Example\n")

a = 5
print("Initial value of a:", a)

# 1) Addition Assignment
a += 5   # a = a + 5
print("After a += 5  ->", a)

# 2) Subtraction Assignment
a -= 5   # a = a - 5
print("After a -= 5  ->", a)

# 3) Multiplication Assignment
a *= 5   # a = a * 5
print("After a *= 5  ->", a)

# 4) Floor Division Assignment
a //= 5  # a = a // 5
print("After a //= 5 ->", a)

# 5) Modulus Assignment
a %= 5   # a = a % 5
print("After a %= 5  ->", a)

# 6) Power Assignment
a **= 5  # a = a ** 5
print("After a **= 5 ->", a)
